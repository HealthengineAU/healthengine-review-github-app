// ---------------------------------------------------------------------------
// pull_request_review_comment.created / .edited (inline suggestions)
// ---------------------------------------------------------------------------

test("review_comment: an Augment 'Fix This' link is stripped via updateReviewComment", async () => {
  const { app, dispatch } = makeApp();
  register(app);
  const octokit = makeOctokit();
  const body =
    "Problem here.\n\n[![Fix This](https://public.augment-assets.com/x.png)](https://augmentcode.com/fix?id=1)";
  const context = makeContext({
    octokit,
    payload: {
      pull_request: { number: 4 },
      comment: { id: 55, body, user: { login: "augmentcode[bot]" } },
    },
  });
  await dispatch("pull_request_review_comment.created", context);

  const patches = octokit.calls.filter(
    (c) => c.method === "rest.pulls.updateReviewComment"
  );
  assert.equal(patches.length, 1);
  assert.equal(patches[0].args.comment_id, 55);
  assert.ok(!patches[0].args.body.includes("augmentcode.com"));
});

