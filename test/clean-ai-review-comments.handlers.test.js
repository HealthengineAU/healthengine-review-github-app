test("review.submitted: a LinearB summary is collapsed via updateReview", async () => {
  const { app, dispatch } = makeApp();
  register(app);
  const octokit = makeOctokit();
  const body =
    "Intro.\n### ✨ PR Review\nFound issues:\n<details><summary>Issue 1</summary>x</details>\nGenerated by LinearB AI and added by gitStream";
  const context = makeContext({
    octokit,
    payload: {
      pull_request: { number: 8 },
      review: { id: 21, body, user: { login: "gitstream-cm[bot]", type: "Bot" } },
    },
  });
  await dispatch("pull_request_review.submitted", context);

  const patches = octokit.calls.filter(
    (c) => c.method === "rest.pulls.updateReview"
  );
  assert.equal(patches.length, 1);
  assert.equal(patches[0].args.review_id, 21);
  assert.ok(patches[0].args.body.includes("LinearB PR Summary"));
});
